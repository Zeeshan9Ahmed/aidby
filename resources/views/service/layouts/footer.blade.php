<script>
    $(document).ready(function() {
        var msg = $('#mSg').val();
        var color = $('#mSg').attr('color');
        if (msg) {
            not(msg, color);
        }
    });

    /** Show notification */
    function not(message, color) {
        notif({
            msg: message,
            type: color
        });
    }

    un_read_notification_count = document.getElementById('un_read_notification_count');
                        
    var baseUrl = $('#baseUrl').val() + "/service";
    setInterval(function() {
        loadNotifications()
    }, 2000);

    loadNotifications();

    notificationDrop = document.getElementById('notificationDrop');
    notificationDrop.addEventListener('click', function () {
        $.ajax({
            type: "GET",
            url: baseUrl + "/update-unread-notifications",
            dataType: "json",

            success: function(response) {

                if (response.status > 0) {
                    un_read_notification_count.innerHTML = "";

                } else {
                    not(response.message, "error");
                }
            },
        });
    }) 

    function loadNotifications() {
        $.ajax({
            type: "GET",
            url: baseUrl + "/notifications",

            dataType: "json",

            success: function(response) {

                if (response.status > 0) {
                    // console.log(response.data)

                    data = response.data
                    notification = document.getElementById('notificationHtml')
                    if (data.length > 0) {
                        var unseenNotifications = data.filter(function(notification) {
                            return notification.seen === "0";
                        });
                        if (unseenNotifications.length > 0 ){
                            un_read_notification_count.innerHTML = `<span class="notification_num">${unseenNotifications.length}</span>`
                        }
                        // console.log( unseenNotifications.length,'u')
                        notifications = data.map((notification) => {
                            return `<div class="notificationItem" >
                                    <a href="${baseUrl}/requests">
                                        <p class="qstion">${notification.title}</p>
                                        <p class="desc">
                                            ${notification.description}
                                        </p>
                                        <p class="time">${convertTimeToAMPM(notification.created_at)}</p>
                                    
                                    <a>
                                </div>`;
                        }).join("");
                        notification.innerHTML = notifications

                    } else {
                        notification.innerHTML = `<div class="notificationItem">
                                    <p class="qstion">No Notifications</p>
                                   
                                </div>`
                    }


                } else {
                    not(response.message, "error");
                }
            },
        });
    }

    function convertTimeToAMPM(date) {
        var dateTimeString = date; // Date and time string

        var date = new Date(dateTimeString); // Create a date object from the string
        var formattedTime = date.toLocaleString('en-US', {
            hour: 'numeric',
            minute: 'numeric',
            hour12: true
        });

        return formattedTime;

    }
